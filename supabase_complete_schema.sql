CREATE TABLE IF NOT EXISTS public.ingredients (
    id TEXT PRIMARY KEY, -- CUID generated by Next.js client
    name TEXT NOT NULL,
    unit TEXT NOT NULL, -- 'kg', 'lt', 'unit', 'gr', 'ml'
    current_stock DOUBLE PRECISION NOT NULL DEFAULT 0,
